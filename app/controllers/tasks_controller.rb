class TasksController < ApplicationController
  # GET /tasks
  # GET /tasks.json
#  before_filter :authenticate_user!

  def index
   # @tasks = Task.all
    if params[:start_datetime].nil?
      if  current_user.site_id.nil?
         @tasks = Task.all
      else
         @tasks = Task.find_by_sql ["select * from tasks where tasks.site_id = ?",current_user.site_id]
      end
    else
      if  current_user.site_id.nil?
        @tasks = Task.find_by_sql ["select * from tasks where tasks.start_time > ?",params[:start_datetime]]
      else
        @tasks = Task.find_by_sql ["select * from tasks where tasks.start_time > ? and tasks.site_id = ?",params[:start_datetime],current_user.site_id]
      end
    end

    respond_to do |format|
      format.html # index.html.erb
      format.json { render json: @tasks }
    end
  end

  # GET /tasks/1
  # GET /tasks/1.json
  def show
    @task = Task.find(params[:id])

    respond_to do |format|
      format.html # show.html.erb
      format.json { render json: @task }
    end
  end

  # GET /tasks/new
  # GET /tasks/new.json
  def new
    @task = Task.new

    respond_to do |format|
      format.html # new.html.erb
      format.json { render json: @task }
    end
  end

  # GET /tasks/1/edit
  def edit
    @task = Task.find(params[:id])
  end

  # POST /tasks
  # POST /tasks.json
  def create
    @task = Task.new(params[:task])

    respond_to do |format|
      if @task.save
        format.html { redirect_to @task, notice: 'Task was successfully created.' }
        format.json { render json: @task, status: :created, location: @task }
      else
        format.html { render action: "new" }
        format.json { render json: @task.errors, status: :unprocessable_entity }
      end
    end
  end

  # PUT /tasks/1
  # PUT /tasks/1.json
  def update
    @task = Task.find(params[:id])

    respond_to do |format|
      if @task.update_attributes(params[:task])
        format.html { redirect_to @task, notice: 'Task was successfully updated.' }
        format.json { head :no_content }
      else
        format.html { render action: "edit" }
        format.json { render json: @task.errors, status: :unprocessable_entity }
      end
    end
  end

  # DELETE /tasks/1
  # DELETE /tasks/1.json
  def destroy
    @task = Task.find(params[:id])
    @task.destroy

    respond_to do |format|
      format.html { redirect_to tasks_url }
      format.json { head :no_content }
    end
  end
end
